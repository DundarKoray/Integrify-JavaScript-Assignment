/* Arithmetic Operator
JavaScript arithmetic operators are addition(+), subtraction(-), multiplication(*), division(/), modulus(%), increment(++) and decrement(--).


let operandOne = 4;
let operandTwo = 3;
*/

let operandOne = 4;
let operandTwo = 3;

console.log(operandOne + operandTwo);
console.log(operandOne - operandTwo);
console.log(operandOne * operandTwo);
console.log(operandOne / operandTwo);
console.log(operandOne % operandTwo);

console.log(++operandOne);
console.log(++operandTwo);

console.log(--operandTwo);
console.log(--operandTwo);
